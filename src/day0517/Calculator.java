package day0517;

public class Calculator {

	public static void main(String[] args) {
		int num1 = 100;
		int num2 = 50;
		
		System.out.println(num1 +  " + " + num2 + " = " + (num1+num2));
		System.out.println(num1 +  " - " + num2 + " = " + (num1-num2));
		System.out.println(num1 +  " * " + num2 + " = " + (num1*num2));
		System.out.println(num1 +  " / " + num2 + " = " + (num1/num2));
	}

}